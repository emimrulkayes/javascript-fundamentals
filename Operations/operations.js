// Arithmetic Operations
let a = 10;
let b = 5;
console.log(a + b); // Output: 15
console.log(a - b); // Output: 5
console.log(a * b); // Output: 50
console.log(a / b); // Output: 2
console.log(a % b); // Output: 0

// Comparison Operations
console.log(a > b); // Output: True
console.log(a < b); // Output: False
console.log(a == 10); // Output: True
console.log(b != 5); // Output: False

// Logical Operations
let isAdult = true;
let hasPermission = false;
console.log(isAdult && hasPermission); // Output: False
console.log(isAdult || hasPermission); // Output: True
console.log(!isAdult); // Output: False

// Ternary Operation
let age = 20;
let access = (age >= 18) ? 'Granted' : 'Denied';
console.log(access); // Outputs: "Granted"
